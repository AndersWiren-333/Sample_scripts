sub timestamp
        {
		# Create a timestamp
		my ($sec,$min,$hour,$mday,$month,$year,$wday,$yday,$isdst) = localtime(time);
		$year=$year+1900;
		$month=$month+1;
		if($month<10)   {       $month = "0"."$month";  }
		if($mday<10)   {       $mday = "0"."$mday";  }
		my $timestamp = "$year"."-"."$month"."-"."$mday"."_"."$hour"."-"."$min"."-"."$sec";

		return($timestamp);
        }
	
return(1);

